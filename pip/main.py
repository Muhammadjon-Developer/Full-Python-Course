# pip - python dasturlash tilidagi paker menejeri kerakli paket va kutubxonalarni o'rnatish uchun (har doim paket va kutubxonalarni eng yangi versiyasi o'rnatib beradi)

# pip yordamida paket va kutubxonalarni o'rnatish uchun terminal ni ochib pip install paketNomi va ENTER

# Paket o'rnatish:
# pip install paketNomi

# Paketning aniq bir versiyasi o'rnatish:
# pip install paketNomi==3.2

# Paketning 3.1 versiyasidan pastroqg'ini o'rnatish:
# pip install paketNomi>=3.1

# Paketni o'chirish:
# pip uninstall paketNomi

# Paketni yangilash:
# pip install --upgrade paketNomi

# Barcha o'rnatilgan paketlarni ko'rish:
# pip list

# Paket haqida aniq va ko'proq ma'lumot olish:
# pip show paketNomi

# Agarda paket nomini aniq bilmasangiz search: argumenti yordamida topishingiz mumkin
# pip search "test"
